string ModelMixture::getName() {
    if (name != "") return name;
    string retname = "MIX";
    retname += OPEN_BRACKET;
    for (iterator it = begin(); it != end(); it++) {
        if (it != begin()) retname += ",";
        retname += (*it)->getName();
    }
    retname += CLOSE_BRACKET;
    return retname;
}

string ModelMixture::getNameParams() {
    if (full_name != "")
        return full_name;
    string retname = "MIX";
    retname += OPEN_BRACKET;
    for (iterator it = begin(); it != end(); it++) {
        if (it != begin()) retname += ",";
        retname += (*it)->getNameParams();
    }
    retname += CLOSE_BRACKET;
    return retname;
}
